"""Render eval results: terminal table, shareable markdown, badge URL, deltas."""

from __future__ import annotations

from .runner import EvalResult

BADGE_TEMPLATE = "https://img.shields.io/badge/Agent--Ready-{score}%25-{color}"


def _color(score: float) -> str:
    if score >= 85:
        return "brightgreen"
    if score >= 70:
        return "green"
    if score >= 50:
        return "yellow"
    return "red"


def badge_url(score: float) -> str:
    return BADGE_TEMPLATE.format(score=score, color=_color(score))


def _verdict_counts(result: EvalResult) -> dict[str, int]:
    counts = {"correct": 0, "partial": 0, "wrong": 0}
    for item in result.items:
        counts[item.verdict] = counts.get(item.verdict, 0) + 1
    return counts


def render_scorecard(result: EvalResult) -> str:
    """Terminal scorecard."""
    counts = _verdict_counts(result)
    lines = [
        "",
        f"  Agent-Ready score: {result.score}/100",
        f"  Agent: {result.agent}",
        f"  {counts['correct']} correct · {counts['partial']} partial · {counts['wrong']} wrong "
        f"({len(result.items)} questions)",
        "",
        "  Category scores:",
    ]
    for category, score in result.by_category().items():
        bar = "█" * int(score // 5)
        lines.append(f"    {category:<14} {score:>5}  {bar}")
    gaps = [i for i in result.items if i.verdict != "correct"]
    if gaps:
        lines.append("")
        lines.append("  Top knowledge gaps:")
        for item in gaps[:5]:
            lines.append(f"    [{item.verdict}] {item.golden.question}")
            lines.append(f"      → {item.golden.artifact} · {item.reason}")
        lines.append("")
        lines.append("  Close the gaps: htc onboard --root <repo>")
    lines.append("")
    lines.append(f"  Badge: {badge_url(result.score)}")
    return "\n".join(lines)


def scorecard_markdown(result: EvalResult) -> str:
    """Shareable markdown scorecard."""
    counts = _verdict_counts(result)
    lines = [
        f"# Agent-Ready Scorecard — {result.score}/100",
        "",
        f"![Agent-Ready]({badge_url(result.score)})",
        "",
        f"**Agent:** `{result.agent}`  ",
        f"**Questions:** {len(result.items)} "
        f"({counts['correct']} correct · {counts['partial']} partial · {counts['wrong']} wrong)",
        "",
        "| Category | Score |",
        "|----------|-------|",
    ]
    for category, score in result.by_category().items():
        lines.append(f"| {category} | {score} |")
    gaps = [i for i in result.items if i.verdict != "correct"]
    if gaps:
        lines += ["", "## Knowledge gaps", ""]
        for item in gaps:
            lines.append(f"- **[{item.verdict}]** {item.golden.question}")
            lines.append(f"  - artifact: `{item.golden.artifact}` — {item.reason}")
    lines += ["", "_Generated by [HTC](https://github.com/norevel/htc) — `htc eval`_", ""]
    return "\n".join(lines)


def render_compare(before: EvalResult, after: EvalResult) -> str:
    """The time-compression delta: before vs after onboarding."""
    delta = round(after.score - before.score, 1)
    sign = "+" if delta >= 0 else ""
    lines = [
        "",
        f"  Agent-Ready: {before.score} → {after.score}  ({sign}{delta})",
        "",
        f"  {'Category':<14} {'before':>7} {'after':>7} {'delta':>7}",
    ]
    before_cats = before.by_category()
    after_cats = after.by_category()
    for category in sorted(set(before_cats) | set(after_cats)):
        b = before_cats.get(category, 0.0)
        a = after_cats.get(category, 0.0)
        d = round(a - b, 1)
        ds = f"+{d}" if d >= 0 else str(d)
        lines.append(f"  {category:<14} {b:>7} {a:>7} {ds:>7}")
    lines.append("")
    return "\n".join(lines)
